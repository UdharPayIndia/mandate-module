package com.rocketpay.mandate.common.basemodule.common.presentation.utils

internal object TimeConstant {
    const val SEC = 1000L
    const val MINUTE = 60L * SEC
    const val HOUR = 60L * MINUTE
    const val DAY = 24L * HOUR
    const val WEEK = 7L * DAY
    const val MONTH = 30L * DAY
    const val YEAR = 12L * MONTH
}